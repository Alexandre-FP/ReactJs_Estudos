// servem para comparar dois valores e/ou variaveis

// igual - ==
// diferente - !=
// menor - <
//menor ou igual - <=
//maior - >
// maior ou igual - >=

// uma comparação sempre retornara verdadeiro ou falso

console.log(1 > 2);
console.log(1 == 1);
console.log("A" == "A");
console.log("A" < "B");

// Para variaveis:

const idade = 16;
const limite = 18;

console.log(idade <= limite);
